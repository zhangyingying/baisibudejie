@interface ZYYVoiceThroughController ()

@property (strong, nonatomic) NSMutableArray *throughArray;


@end

@implementation ZYYVoiceThroughController

- (void)viewDidLoad
{
    [super viewDidLoad];
    
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
    self.navigationController.navigationBar.barTintColor = RGBA(120, 0, 140, 1);
    [self voiceThroughAfhttpRequest];
}

- (void)voiceThroughAfhttpRequest{
    
    self.throughArray = [NSMutableArray array];
    AFHTTPRequestOperationManager *manager = [AFHTTPRequestOperationManager manager];
    [manager GET:kVoiceLatestAPI parameters:nil success:^(AFHTTPRequestOperation *operation, id responseObject) {
        
        NSMutableArray *dataArray = [responseObject objectForKey:@"list"];
        for (NSMutableDictionary *dic in dataArray) {
            ZYYHomeModel *model = [[ZYYHomeModel alloc] init];
            [model setValuesForKeysWithDictionary:dic];
            [self.throughArray addObject:model];
            
        }
        [self.tableView reloadData];
        
    } failure:^(AFHTTPRequestOperation *operation, NSError *error) {
        UIAlertView *alertView = [[UIAlertView alloc] initWithTitle:@"提示" message:@"数据请求失败" delegate:nil cancelButtonTitle:nil otherButtonTitles:@"确定", nil];
        [alertView show];
        
    }];
    
    
}



